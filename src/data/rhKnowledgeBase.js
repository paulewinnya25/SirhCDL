// Fonctions utilitaires pour l'agent vocal
export const AGENT_UTILS = {
  // Générer une salutation aléatoire
  getRandomGreeting: () => {
    const greetings = RH_KNOWLEDGE_BASE.culture.salutations;
    return greetings[Math.floor(Math.random() * greetings.length)];
  },

  // Générer une phrase d'encouragement
  getRandomEncouragement: () => {
    const encouragements = RH_KNOWLEDGE_BASE.culture.encouragement;
    return encouragements[Math.floor(Math.random() * encouragements.length)];
  },

  // Générer une touche d'humour
  getRandomHumor: () => {
    const humor = RH_KNOWLEDGE_BASE.culture.humour;
    return humor[Math.floor(Math.random() * humor.length)];
  },

  // Rechercher dans la base de connaissances
  searchKnowledge: (query) => {
    const lowerQuery = query.toLowerCase();
    
    // Recherche dans les FAQ
    for (const [question, reponse] of Object.entries(RH_KNOWLEDGE_BASE.faq)) {
      if (question.toLowerCase().includes(lowerQuery) || reponse.toLowerCase().includes(lowerQuery)) {
        return { type: 'faq', question, reponse };
      }
    }

    // Recherche dans les politiques
    if (lowerQuery.includes('congé') || lowerQuery.includes('vacance')) {
      return { type: 'politique', categorie: 'conges', data: RH_KNOWLEDGE_BASE.politiques.conges };
    }

    if (lowerQuery.includes('salaire') || lowerQuery.includes('rémunération') || lowerQuery.includes('prime')) {
      return { type: 'politique', categorie: 'remuneration', data: RH_KNOWLEDGE_BASE.politiques.remuneration };
    }

    if (lowerQuery.includes('horaire') || lowerQuery.includes('travail')) {
      return { type: 'politique', categorie: 'horaires', data: RH_KNOWLEDGE_BASE.politiques.horaires };
    }

    // Recherche dans les services
    for (const [service, description] of Object.entries(RH_KNOWLEDGE_BASE.services)) {
      if (lowerQuery.includes(service) || description.toLowerCase().includes(lowerQuery)) {
        return { type: 'service', service, description };
      }
    }

    // Recherche dans la formation
    if (lowerQuery.includes('formation') || lowerQuery.includes('développement') || lowerQuery.includes('carrière')) {
      return { type: 'formation', data: RH_KNOWLEDGE_BASE.formation };
    }

    // Recherche dans les procédures RH
    if (lowerQuery.includes('pointage') || lowerQuery.includes('pointeuse') || lowerQuery.includes('carte')) {
      return { type: 'procedure', categorie: 'pointage', data: RH_KNOWLEDGE_BASE.procedures.pointage };
    }

    if (lowerQuery.includes('recrutement') || lowerQuery.includes('embauche') || lowerQuery.includes('intégration')) {
      return { type: 'procedure', categorie: 'recrutement', data: RH_KNOWLEDGE_BASE.procedures.recrutement };
    }

    if (lowerQuery.includes('paie') || lowerQuery.includes('salaire') || lowerQuery.includes('paiement')) {
      return { type: 'procedure', categorie: 'paie', data: RH_KNOWLEDGE_BASE.procedures.paie };
    }

    if (lowerQuery.includes('discipline') || lowerQuery.includes('sanction') || lowerQuery.includes('faute')) {
      return { type: 'procedure', categorie: 'discipline', data: RH_KNOWLEDGE_BASE.procedures.discipline };
    }

    if (lowerQuery.includes('formation') || lowerQuery.includes('santymed') || lowerQuery.includes('réglementaire')) {
      return { type: 'procedure', categorie: 'formation', data: RH_KNOWLEDGE_BASE.procedures.formation };
    }

    return null;
  }
};
